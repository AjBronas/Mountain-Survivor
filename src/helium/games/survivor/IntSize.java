package helium.games.survivor;

public class IntSize {

	public float width = 0;
	public float height = 0;

	public IntSize() {

	}
	
	public IntSize( int width, int height ) {

		this.width = width;
		this.height = height;
	} 
	
	public IntSize( float width, float height ) {

		this.width = width;
		this.height = height;
	} 
}
